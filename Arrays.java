public class Arrays {
    public static void main(String args[]) {
        int[] number;
        number = new int[4];
        number[0] = 6;
        number[1] = 7;
        number[2] = 8;
        number[3] = 90;

        for (int i = 0; i < number.length; i++) {
            System.out.println(number[i]);
        }
    }
}
